export type MonitorType = 'LCS' | 'Reference' | 'Inactive';

export type ViewMode = 'monitors' | 'coverage';

export interface MonitorStation {
  id: string;
  name: string;
  city: string;
  country: string;
  iso2: string;
  latitude: number;
  longitude: number;
  type: MonitorType;
  status: 'active' | 'inactive';
  network: string;
  operator: string;
  equipment: string;
  manufacturer: string;
  pollutants: string[];
  resolution: string;
  transmission: string;
  site: string;
  landUse: string;
  deployed: string;
  calibrationLastDate: string;
  calibrationMethod: string;
  uptime30d: string;
  publicData: 'Yes' | 'No';
  organisation: string;
  coLocation: string;
  coLocationNote: string;
  lastActive: string;
  viewDataUrl?: string;
}

export interface CountryCoverage {
  id: string;
  country: string;
  iso2: string;
  monitors: MonitorStation[];
}

